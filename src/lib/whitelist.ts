export function isEmailWhitelisted(email: string | null | undefined): boolean {
  if (!email) return false;
  const normalized = email.trim().toLowerCase();
  return WHITELIST_EMAILS.some(entry => entry.email.trim().toLowerCase() === normalized);
}

export function getWhitelistEntry(email: string | null | undefined): WhitelistEntry | undefined {
  if (!email) return undefined;
  const normalized = email.trim().toLowerCase();
  return WHITELIST_EMAILS.find(entry => entry.email.trim().toLowerCase() === normalized);
}
